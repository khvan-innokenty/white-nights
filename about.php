            <h1>О санатории</h1>
            <p>ООО «ММЦ «СОГАЗ» Обособленное подразделение «Белые ночи» (санаторий «Белые ночи» - ММЦ «СОГАЗ») является санаторием общетерапевтического профиля с наличием специализированных отделений. Именитая здравница расположена неподалеку от Сестрорецка на побережье Финского залива и предоставляет медицинские и оздоровительные услуги жителям Российской Федерации, главным образом жителям Санкт-Петербурга. Благодаря особому вниманию к качеству медицинского обслуживания с ориентацией на европейский уровень, санаторию на протяжении многих лет удается поддерживать высокий рейтинг среди санаторно-курортных учреждений Ленобласти и Северо-Западного региона.</p>
            <p>Учредителем и собственником имущества санатория является Санкт-Петербург в лице уполномоченных государственных органов: Комитета по управлению городским имуществом и Комитета по здравоохранению.</p>
            <p>Санаторий введен в эксплуатацию в мае 1988 года. Заказчиком строительства санаторного комплекса выступало Четвертое главное управление при Минздраве СССР – в то время ведущее в стране по уровню организации здравоохранения населения, эталон отечественного здравоохранения.</p>

