            <h2>Медицинский профиль</h2>
            <p>Основными профильными направлениями работы санатория являются патологии сердечно-сосудистой системы, центральной и периферической нервной системы, опорно-двигательного аппарата. Большое значение мы также уделяем вопросам первичной профилактики заболеваний, формированию и поддержанию здорового образа жизни.</p>
            <p>Для оказания медицинских услуг на высоком профессиональном уровне, с использованием новейших достижений медицинской науки в санаторий, привлекаются специалисты, ученые научных медицинских центров Санкт-Петербурга: НИИ кардиологии, Института биорегуляции и геронтологии, МАПО, Петербургского медицинского университета им. акад. И.П. Павлова. В санатории все пациенты проходят первичный осмотр, на основании которого лечащий врач назначает дополнительные обследования и курс лечения на весь период проживания.</p>
            <p class="attention">Не забудьте взять с собой Вашу санаторно-курортную карту. Санаторно-курортная карта оформляется в поликлинике по месту проживания. Врач оценивает наличие показаний для прохождения санаторно-курортного лечения. Карту следует оформлять не ранее, чем за два месяца до приезда в санаторий.</p>

            <h2>Территория</h2>
            <p>Как упомянуто выше, санаторий «Белые ночи» расположен в Курортном районе Санкт-Петербурга, климатические и природные особенности которого широко известны своими многочисленными лечебными факторами.  Умеренный климат, чистый морской воздух, наполненный фитонцидами хвойных деревьев и залив с песчаным пляжем – все это дополняет лечение наших пациентов особым комфортом и несет в себе оздоровительный смысл. Столь обширные зеленные владения позволяют санаторию осваивать новые методики восстановления и лечения. Одним из таковых направлений является скандинавская ходьба и терренкур.</p>
            <p>Здание санатория, построенное из красного кирпича, отлично вписывается в сосновый массив, растущий на песчаных дюнах, которые являются уникальным памятником природы. Средний возраст соснового массива составляет 56 лет. Территория парка, в центре которого расположен главный корпус санатория, раскинулась на площади 33, 68 гектара.</p>
            <p class="attention">Консультативно-диагностическое отделение нашего комплекса оборудовано современной аппаратурой для исследования состояния здоровья человека. Благодаря наличию собственной лаборатории, широкий перечень диагностических методик позволяет уточнить диагноз пациента для назначения более полного и рационального санаторно-курортного лечения.</p>

